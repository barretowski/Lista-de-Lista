public class Cidade {
    private Cidade inicio;
    private String nome;

    public Cidade(){
        this.inicio = null;
        this.nome ="";
    }
    public Cidade getInicio(){
        return inicio;
    }
    public void setInicio(Cidade inicio){
        this.setInicio(inicio);
    }
    public String getNome(){
        return nome;
    }
    public void setNome(String nome){
        this.nome = nome;
    }
}
